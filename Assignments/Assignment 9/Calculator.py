import math

class Calculator:
    def addition(self, x, y):
        return x + y

    def subtraction(self, x, y):
        return x - y

    def division(self, x, y):
        return x / y

    def multiplication(self, x, y):
        return x * y

    def exponent(self, x, y):
        return x ** y

    def square_root(self, x):
        return x ** (1/2)

    def negate(self, x):
        return x * -1